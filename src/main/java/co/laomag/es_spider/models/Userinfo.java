package co.laomag.es_spider.models;

public class Userinfo {
    private String userId;

    private String userName;

    private String userPassword;

    private String userCreadtime;

    private String userUpdatetime;

    private String userEmail;

    private String userPhone;

    private String userIoc;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }

    public String getUserCreadtime() {
        return userCreadtime;
    }

    public void setUserCreadtime(String userCreadtime) {
        this.userCreadtime = userCreadtime;
    }

    public String getUserUpdatetime() {
        return userUpdatetime;
    }

    public void setUserUpdatetime(String userUpdatetime) {
        this.userUpdatetime = userUpdatetime;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }

    public String getUserPhone() {
        return userPhone;
    }

    public void setUserPhone(String userPhone) {
        this.userPhone = userPhone;
    }

    public String getUserIoc() {
        return userIoc;
    }

    public void setUserIoc(String userIoc) {
        this.userIoc = userIoc;
    }
}